package hr.algebra.model;

/**
 *
 * @author lorena
 */
public class Super {
    transient int id;
    transient String name;

    public Super(String name) {
        this.name = name;
    }

    public Super(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
    
    
    
    
}
